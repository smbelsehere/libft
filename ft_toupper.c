#include "libft.h"

int	ft_toupper(int c)
{
	if ((c >= 97 && c <= 120))
		(c -= 32);
	return (c);
}

int	main(void)
{
	int	c = 'w';
	
	printf("%c", ft_toupper(c));
	return (0);
}